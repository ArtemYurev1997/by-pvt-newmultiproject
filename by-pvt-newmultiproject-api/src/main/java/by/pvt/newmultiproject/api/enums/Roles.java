package by.pvt.newmultiproject.api.enums;

public enum Roles {
    ADMIN("Admin"),
    CLIENT("Client");

    private String role;

    Roles(String role) {
        this.role = role;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    @Override
    public String toString() {
        return role;
    }
}
